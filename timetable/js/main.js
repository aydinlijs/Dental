function PlaceTimeTable(index) {

	//Uygun timetableleri doldurur
	for (var j = 0; j < 7; j++) {
		for (var i = 0; i < doctors[index][j].length; i++) {
			var color;
			var img;
			var type;
			var dentist = doctors[index][j][i];
			if (dentist[0] == 'Carolyn Hunt') {
				color = '#0788A7';
				img = 'carolyn';
				type = 'Dental Hygienist';
			}
			else if (dentist[0] == 'Eric Synder') {
				color = '#18AFD3';
				img = 'eric';
				type = 'Dentist';
			}
			else if (dentist[0] == 'Christina Holland') {
				color = '#F57E57';
				img = 'christina';
				type = 'Pediatric Dentist';
			}
			var lengthOfDiv = dentist[2].getHours() - dentist[1].getHours();
			var startPosition = dentist[1].getHours() - 8;
			$("<div></div>", {
				class: "doctor",
				style: "background: " + color + "; height:" + 100 * lengthOfDiv + "px",
			}).append('<img src=img/doctors/' + img + '.jpg>').append('<h5>' + dentist[0] + '</h5>').append('<h6>' + type + '</h6>').appendTo($("#t" + index + " table tbody tr:nth-child(" + (startPosition + 1) + ") td:nth-child(+" + (j + 2) + ")"))
			$("<div></div>", {
				class: "doctor",
				style: "background: " + color + "; height:" + 100 * lengthOfDiv + "px",
			}).append('<img src=img/doctors/' + img + '.jpg>').append('<h5>' + dentist[0] + '</h5>').append('<h6>' + type + '</h6>').appendTo($("#a" + index + " table tbody tr:nth-child(" + (startPosition + 1) + ") td:nth-child(+" + (j + 2) + ")"))
		}
	}
}
$(document).ready(function () {
	for (var a = 0; a < 5; a++) {
		PlaceTimeTable(a);
	}
	for (var b = 0; a < 5; a++) {
		PlaceTimeTable(b);
	}
})
